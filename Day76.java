
package niingg_31;


public class Day76 {
    public static void main(String[] args) {
          
        
        String nama = "Haii, Ningsih!";
        String salam = "Selamat siang.";
        String pesan = "Jangan lupa belajar ya!";
        
       
        
        String namaUpperCase = nama.toUpperCase();
        String salamUpperCase = salam.toUpperCase();
        String pesanUpperCase = pesan.toUpperCase();

        
        System.out.println("== Contoh Penggunaan toUpperCase() ==");
        
        
        System.out.println("String Asli 1: " + nama);
        System.out.println("String Huruf Besar 1: " + namaUpperCase);
        
        System.out.println("\nString Asli 2: " + salam);
        System.out.println("String Huruf Besar 2: " + salamUpperCase);
        
        System.out.println("\nString Asli 3: " + pesan);
        System.out.println("String Huruf Besar 3: " + pesanUpperCase);
    }
    
}
